package com.dev.vo;

public class MemberVO {
	private String id;
	private String pwd;
	private String name;
	
	public String getId() {
		return id;
	}
	public void setId(String tId) {
		this.id = tId;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String tPwd) {
		this.pwd = tPwd;
	}
	public String getName() {
		return name;
	}
	public void setName(String tName) {
		this.name = tName;
	}
	
}
